#include "../inc/libft.h"

char	*ft_strcpy(char *d, const char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		d[i] = src[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
