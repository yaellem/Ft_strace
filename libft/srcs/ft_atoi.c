#include "../inc/libft.h"

int		ft_atoi(const char *r)
{
	long long	nb;
	long long	x;

	nb = 0;
	while ((*r >= 9 && *r <= 13) || *r == 32)
		r++;
	if (*r == '+')
		r++;
	else if (*r == '-')
	{
		x = 1;
		r++;
	}
	while (*r >= 48 && *r <= 57)
	{
		nb = (nb * 10 + *r) - 48;
		r++;
	}
	if (x == 1)
		nb = -nb;
	return ((int)nb);
}
